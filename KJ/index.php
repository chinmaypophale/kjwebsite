                  <h2 class="text-uppercase">What is NAV ?</h2>
                  <p class="item-intro text-muted">Lorem ipsum dolor sit amet consectetur.</p>
                  <img class="img-fluid d-block mx-auto" src="img/nav.jpg" alt="">
                  <p>NAV means the Net Asset Value of a mutual fund. It is arrived at by subtracting the liabilities and expenses of the fund from the value of its assets. When divided by the number of outstanding shares in the fund, NAV stands for the average price per share. For example, if a fund&rsquo;s assets are valued at Rs. 3 crore and it has liabilities of Rs. 58 lakh, then the net book value of the fund would be Rs. 2.42 crore. If there are 5 lakh outstanding shares in the fund on a given day, then the NAV for the day would be Rs. 48.4.</p>
<p>So to put it in a formula: <strong>NAV of a mutual fund = (Value of assets &ndash; value of liabilities)/Number of outstanding units or shares</strong></p>
<p>This NAV could change on a day-to-day basis depending on the change in assets and liabilities, the number of shares, and the price of the shares in the market. For this reason, NAVs are published by most mutual funds at the end of the day, after the market closes. Fund managers need time to calculate the values of the fund&rsquo;s shares based on the rates at the time of market closure.</p>
