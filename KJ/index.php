                  <p><strong>How to invest in Mutual Funds</strong></p>
<p>So, you know what <u>Mutual Funds</u> are and their associated costs. It&rsquo;s time to know more about how to invest in them.</p>
<p><strong>Asset allocation</strong></p>
<p>The first thing here is to understand what kind of portfolio you need. Your funds will need to be divided between different asset classes to achieve the returns that you want. This is known as <strong>asset allocation</strong>. The ideal asset allocation route would help you to invest in a number of funds that are based on your risk profile. Your risk profile will also help determine the extent to which you should invest in each asset segment such as equity and debt.</p>
<p>Your asset allocation should have a healthy mix of high-risk and low-risk components. The usual rule is that the percentage of funds you allocate to low-risk debt instruments should be equal to your age. For instance, if you are a 30 year old, then 30% of your fund allocation should go toward debt instruments. This will cushion you against any downturns due to investments in high-risk assets. This might be true when you are young but as you grow older, you must reduce your high-risk investments. A golden rule here is that the younger you are, the more you can invest in equities and other high-risk Mutual Funds. Up to a certain age, your risk profile can be moderately high as you have certain flexibilities to invest in high-risk, high-return funds without getting too worried about potential losses.</p>
<p><strong>Shortlisting fund types</strong></p>
<p>Shortlisting and zeroing in on the right funds represents the most important part of investing in Mutual Funds. Once you are done with the asset allocation that best reflects your needs, the next step is to look for and compare different Mutual Funds on the basis of their <strong>past performance</strong> and <strong>investment philosophy</strong>. For this, you should refer to the <strong>shareholder reports</strong> and <strong>prospectuses</strong> provided by AMCs. The prospectus will detail the information related to the Mutual Fund from a legal perspective while the shareholder report can help you understand the past performance and consistency of returns.</p>
<p>Before investing in a fund, you should first be certain about what your <strong>ultimate financial goals</strong> are. Are you investing to substitute your current income or planning for retirement or are you looking to save for child&rsquo;s marriage?</p>
<p>Next, you need to determine what the <strong>horizon</strong> for these goals will be. The more money you need, the more risk you might need to take if you don&rsquo;t have much time. You can afford to take lower risks if your goal is a long-term one. However, understand that when you invest in high-risk funds for the long term, the risks will become considerably lower as your goal nears. You should choose your Mutual Funds accordingly.</p>
<p>Some Mutual Funds are open-ended while others are close-ended. In case of the latter, you won&rsquo;t be able to liquidate the funds until the fund has matured. Therefore, you need to be careful about the time frame you&rsquo;re investing for. In general, the shorter the period of investment, the lower the risks that you should take, while a higher time frame will help you overcome downturns in case of high-risk instruments.</p>
<p>Finally, ensure that your <strong>risk profile </strong>is right. This may seem daunting but once you have charted out your future requirements and the time frame, you can find out what kind of risk profile you are comfortable with. Are you comfortable with the dynamics of the stock market and can you accept both ups and downs? Or are you looking for safe and assured bets that will meet your needs and still keep you safe? These depend on your personal outlook. If you aren&rsquo;t comfortable with an asset class even though it&rsquo;s aligned directly with your goals, you should drop that option.</p>
