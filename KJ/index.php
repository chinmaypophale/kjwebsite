                  <h2 class="text-uppercase">Why Mutual Funds?</h2>
                  <p class="item-intro text-muted">Lorem ipsum dolor sit amet consectetur.</p>
                  <img class="img-fluid d-block mx-auto"  alt="">
                  <p>When you can invest in stocks or government securities on your own, you may feel that you don&rsquo;t need professional help to manage such investments. You could be wrong. Investing in the markets is not simply choosing stocks and forgetting about them. The process becomes fairly complex when more than a couple of stocks and fixed-income securities are involved and almost impossible for any run-of-the-mill investor. With professionally managed Mutual Funds, you can be assured that your investments are managed by people with many years of experience with market analysis. They will have enough knowledge to take calls on buying and selling those stocks and other investments. You might not actually have that kind of knowledge or time to handle individual stock or fixed-income investments. Fund managers can easily identify laggards and prevent the portfolio from becoming stagnant due to underperformers. With Mutual Funds, you get:</p>
                    <p><strong>Right amount of diversification</strong></p>
                    <p>Mutual Funds allow you to diversify your investment across assets and asset classes, something that is very difficult to do on your own.</p>
                    <p><strong>Flexibility</strong></p>
                    <p>You are given options to pick any type of fund as per your risk profile, and bundle all of them into a single portfolio. Data about the performance of the funds is easily available for you to take those buy and sell calls.</p>
                    <p><strong>Professional managers</strong></p>
                    <p>The fund managers of Mutual Funds are usually highly experienced in their respective fields and will have years of experience handling different types of assets. And what&rsquo;s more, you will be provided with the profile of your fund manager so that you know who&rsquo;s actually handling your hard-earned money.</p>
                    <p><strong>Accessibility</strong></p>
                    <p>There&rsquo;s nothing more convenient than a central database providing you with all the required information and even highlighting what&rsquo;s best for you. This is possible through Mutual Funds.</p>
                    <p><strong>Liquidity</strong></p>
                    <p>Your Fixed Deposit may be offering decent returns with little option for liquidity, or the stock market may give you decent returns with easy liquidity and a high probability of losses. A Mutual Fund is a fine balance between the two offering you good returns while providing you with decent liquidity.</p>
                    <p><strong>Tax benefits</strong></p>
                    <p>There are tax benefits associated with Mutual Funds. You need to invest in <strong>Equity Linked Savings Schemes (ELSS)</strong> of Mutual Funds for the same.</p>
